//! Criterion benchmarks for lightweight packet summaries.

#![allow(missing_docs)]

// REQ: NFR-PERF-PCAP

use criterion::{black_box, criterion_group, criterion_main, Criterion};
use tracemux_core::packet_summary::{summarize_pcap_packet, LINKTYPE_ETHERNET};

const ETHERNET_IPV4_TCP_PACKET: &[u8] = &[
    0xaa, 0xbb, 0xcc, 0xdd, 0xee, 0xff, 0x00, 0x11, 0x22, 0x33, 0x44, 0x55, 0x08, 0x00, 0x45, 0x00,
    0x00, 0x2c, 0x00, 0x00, 0x00, 0x00, 0x40, 0x06, 0x00, 0x00, 0xc0, 0xa8, 0x01, 0x0a, 0xc0, 0xa8,
    0x01, 0x14, 0x30, 0x39, 0x01, 0xbb, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x00, 0x50, 0x02,
    0x10, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01, 0x02, 0x03, 0x04,
];

fn bench_packet_summary(c: &mut Criterion) {
    c.bench_function("packet_summary_ethernet_ipv4_tcp", |b| {
        b.iter(|| {
            black_box(
                summarize_pcap_packet(LINKTYPE_ETHERNET, black_box(ETHERNET_IPV4_TCP_PACKET))
                    .expect("packet summary"),
            );
        });
    });
}

criterion_group!(benches, bench_packet_summary);
criterion_main!(benches);
